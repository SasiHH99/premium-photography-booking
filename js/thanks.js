document.addEventListener("DOMContentLoaded", () => {
  const shell = document.querySelector("[data-thanks-page]");
  if (!shell) return;

  const lang = shell.dataset.lang === "hu" ? "hu" : "de";
  const type = shell.dataset.thanksType === "contact" ? "contact" : "booking";
  const params = new URLSearchParams(window.location.search);
  const state = params.get("state") === "partial" ? "partial" : "success";
  const copy = THANKS_COPY[lang][type];

  const title = document.getElementById("thanksTitle");
  const body = document.getElementById("thanksBody");
  const badge = document.getElementById("thanksBadge");
  const statusBox = document.getElementById("thanksStatus");
  const stepTitle = document.getElementById("thanksSectionTitle");
  const actionPrimary = document.getElementById("thanksActionPrimary");
  const actionSecondary = document.getElementById("thanksActionSecondary");

  if (type === "booking") {
    title.textContent = state === "partial" ? copy.titlePartial : copy.titleSuccess;
    body.textContent = state === "partial" ? copy.bodyPartial : copy.bodySuccess;
    badge.textContent = state === "partial" ? copy.badgePartial : copy.badgeSuccess;
    statusBox.classList.toggle("is-partial", state === "partial");
    statusBox.classList.toggle("is-success", state !== "partial");
  } else {
    title.textContent = copy.titleSuccess;
    body.textContent = copy.bodySuccess;
    badge.textContent = copy.badgeSuccess;
    statusBox.classList.add("is-success");
  }

  stepTitle.textContent = copy.sectionTitle;
  actionPrimary.textContent = copy.actionPrimary;
  actionSecondary.textContent = copy.actionSecondary;

  const actionPrimaryHref = type === "booking"
    ? "portfolio.html"
    : (lang === "hu" ? "arak.html" : "preise.html");
  const actionSecondaryHref = type === "booking"
    ? (lang === "hu" ? "kapcsolat.html" : "kontakt.html")
    : "portfolio.html";

  actionPrimary.href = actionPrimaryHref;
  actionSecondary.href = actionSecondaryHref;

  document.getElementById("summaryOneLabel").textContent = copy.summaryOneLabel;
  document.getElementById("summaryOneValue").textContent = copy.summaryOneValue;
  document.getElementById("summaryOneCopy").textContent = copy.summaryOneCopy;
  document.getElementById("summaryTwoLabel").textContent = copy.summaryTwoLabel;
  document.getElementById("summaryTwoValue").textContent = copy.summaryTwoValue;
  document.getElementById("summaryTwoCopy").textContent = copy.summaryTwoCopy;
  document.getElementById("summaryThreeLabel").textContent = copy.summaryThreeLabel;
  document.getElementById("summaryThreeValue").textContent = copy.summaryThreeValue;
  document.getElementById("summaryThreeCopy").textContent = copy.summaryThreeCopy;
  document.getElementById("stepOneTitle").textContent = copy.stepOneTitle;
  document.getElementById("stepOneCopy").textContent = copy.stepOneCopy;
  document.getElementById("stepTwoTitle").textContent = copy.stepTwoTitle;
  document.getElementById("stepTwoCopy").textContent = copy.stepTwoCopy;
  document.getElementById("stepThreeTitle").textContent = copy.stepThreeTitle;
  document.getElementById("stepThreeCopy").textContent = copy.stepThreeCopy;

  const details = document.getElementById("thanksDataList");
  if (type === "booking" && details) {
    const date = params.get("date") || "-";
    const packageName = params.get("package") || "-";
    const email = params.get("email") || "-";

    details.innerHTML = `
      <div>
        <dt>${copy.infoDate}</dt>
        <dd>${date}</dd>
      </div>
      <div>
        <dt>${copy.infoPackage}</dt>
        <dd>${packageName}</dd>
      </div>
      <div>
        <dt>${copy.infoEmail}</dt>
        <dd>${email}</dd>
      </div>
    `;
  } else if (details) {
    details.remove();
  }
});
